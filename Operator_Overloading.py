# operator overloading
# '+' operator in python is overloaded to perform multiple things




# '+' will add two numbers
x =10
y=11
print("Addition is:",x+y)

# '+' will concatenate two strings
s1 ='Hello'
s2='world'
print("Concatenate two strings:",s1+' '+s2)

# '+' will add two lists
l1=[1,2,3]
l2=[5,6,7]
print("Add two list elements:",l1+l2)
