#booleans-True or false
print(3>4)
print(1<2)

d = 10>50
print(d)
print(type(d))

e = True
print(e)
print(type(e))

#Represent numbers in differrent formats- Binary,octal,hexademical

a = 10
print(bin(a))
print(hex(a))
