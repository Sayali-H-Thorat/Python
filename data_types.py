#Type conversion/Type casting
#converting one datatype into another

#1.Convert float into integer
a = 10.7
print(a)
print(type(a))

b = int(a)
print(b)
print(type(b))

#2.Convert int to float
x =20
print(x)
print(type(x))

y =float(x)
print(y)
print(type(y))

#3.Convert string to integer
z = '100'
print(z)
print(type(z))

a = int(z)
print(a)
print(type(a))

#4.Convert int to complex

a = 10
print(a)
print(type(a))

